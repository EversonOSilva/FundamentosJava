package Streams;

public class Produtos {

    final String nome;
    final double valorFrete;
    final double preco;
    final double desconto;

    public Produtos(String nome, double valorFrete, double preco, double desconto) {
        this.nome = nome;
        this.valorFrete = valorFrete;
        this.preco = preco;
        this.desconto = desconto;
    }
}
